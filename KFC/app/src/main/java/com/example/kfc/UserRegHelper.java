package com.example.kfc;

public class UserRegHelper {
    String email="",pass="",firstname="",lastname="",address="",postal="",phoneNo="",country="",city="";

    public UserRegHelper() {
    }

    public UserRegHelper(String email, String pass, String firstname, String lastname, String address, String postal, String phoneNo, String country, String city) {
        this.email = email;
        this.pass = pass;
        this.firstname = firstname;
        this.lastname = lastname;
        this.address = address;
        this.postal = postal;
        this.phoneNo = phoneNo;
        this.country = country;
        this.city = city;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPostal() {
        return postal;
    }

    public void setPostal(String postal) {
        this.postal = postal;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
